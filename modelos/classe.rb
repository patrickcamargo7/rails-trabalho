class Classe
  attr_accessor nome, habilidade, vida, ataque, defesa, inteligencia, forca

  def initialize(nome, habilidade, vida, ataque, defesa, inteligencia, forca)
    @nome = nome
    @habilidade = habilidade
    @vida = vida
    @ataque = ataque
    @defesa = defesa
    @inteligencia = inteligencia
    @forca = forca
  end
end
